                                        <h2 class="text-xl font-semibold text-primary-600">Isn't Masonry just a place
                                            where businessmen make deals?</h2>
                                        <p class="mt-2">
                                            No. In fact, most Masons believe that to trade with a Brother Mason only
                                            because he is a Mason is unmasonic. Even more importantly, anyone who
                                            attempts to join a Lodge solely for business reasons will not be given a
                                            petition.<br>
                                            <br>
                                            Masons, however, are friends, and it is not surprising that many Masons do
                                            trade with Brothers. For one thing, they are dealing with people that are of
                                            good character and can be trusted, which is no small statement in the modern
                                            marketplace.<br>
                                            <br>
                                            But Masonry is not a "place to network". Yes, some men do view one of the
                                            benefits of membership as an additional source of customers or partners, but
                                            few would say that is the only reason they became Masons. The work involved
                                            in the degrees alone would make this a poor investment - better to join the
                                            Rotary Club or other business group.<br>
                                        </p>
