                                        <h2 class="text-xl font-semibold text-primary-600">Do I have to be invited to
                                            become a Mason?</h2>
                                        <p class="mt-2">
                                            Don't wait to be invited - you will die waiting.<br>
                                            <br>
                                            Some men are surprised that no one has ever asked them to become a Mason.
                                            They may even feel that the Masons in their town don't think they are "good
                                            enough" to join. But it doesn't work that way. For hundreds of years, Masons
                                            have been forbidden to ask others to join the fraternity. We can talk to
                                            friends about Masonry. We can tell them about what Masonry does. We can tell
                                            them why we enjoy it. But we can't ask, much less pressure, anyone to join.<br>
                                            <br>
                                            There's a good reason for that. It isn't that we're trying to be exclusive.
                                            But becoming a Mason is a very serious thing. Joining Masonry is making a
                                            permanent life commitment to live in certain ways. We've listed most of them
                                            above -- to live with honor and integrity, to be willing to share with and
                                            care about others, to trust each other, and to place ultimate trust in God.
                                            No one should be "talked into" making such a decision.<br>
                                            <br>
                                            Masons are prohibited from actively recruiting or asking non-Masons to join
                                            the fraternity, to insure that candidates come of their own free will.<br>
                                            <br>
                                            As with many things Masonic, there are some exceptions to this rule. Some
                                            Grand Lodges allow solicitation, provided it is low-key and with the strict
                                            provision that no pressure be applied.<br>
                                            <br>
                                            Note: The Grand Lodge of Indiana allows low-key solicitation. Still, you
                                            don't need to be invited in any jurisdiction, and if you're interested, act.<br>
                                        </p>
