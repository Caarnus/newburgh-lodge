                                        <h2 class="text-xl font-semibold text-primary-600">I am interested, but how do I
                                            proceed? What are the requirements?</h2>
                                        <p class="mt-2">
                                            The person who wants to join Masonry must be a man (it's a fraternity),
                                            sound in body and mind, who believes in God, is at least the minimum age
                                            required by Masonry in his state, and has a good reputation. (Incidentally,
                                            the "sound in body" requirement -- which comes from the stonemasons of the
                                            Middle Ages -- doesn't mean that a physically challenged man cannot be a
                                            Mason; many are).<br>
                                            <br>
                                            Those are the only "formal" requirements. But there are others, not so
                                            formal. He should believe in helping others. He should believe there is more
                                            to life than pleasure and money. He should be willing to respect the
                                            opinions of others. And he should want to grow and develop as a human being.<br>
                                            <br>
                                            If you know a Mason, ask him about membership. He will be glad to tell you
                                            all about the Craft and the local lodge, and give you a petition if you wish
                                            to join.<br>
                                            <br>
                                            If you do not know a Mason, drop a letter to the local lodge, and one of the
                                            officers will call you (or call the lodge, though you may not get an answer
                                            unless someone is actually there).<br>
                                            <br>
                                            Typically, the process is as follows:<br>
                                            <br>
                                            The applicant fills out a petition. The petition asks for two sponsors,
                                            though if you meet and talk with the officers, they can usually find
                                            sponsors or act as sponsors themselves if you do not know anyone in the
                                            lodge.<br>
                                            <br>
                                            The petition is read at the lodge during the next business meeting, which
                                            for many lodges is during the first week of the month. A committee is formed
                                            to investigate the candidate. The petition also asks for two character
                                            references.<br>
                                            <br>
                                            The committee meets with the candidate to answer questions, ascertain that
                                            he meets the criteria for membership, and find out a little about him. This
                                            is not a "grilling session," but rather a friendly and casual chat to make
                                            certain that the candidate has been properly informed about Masonry and was
                                            not improperly solicited. The committee also contacts the character
                                            references listed on the petition (typically asking if they know any reason
                                            why the candidate should not be accepted, etc.)<br>
                                            <br>
                                            The committee reports back to the lodge during the next business meeting and
                                            the candidate is voted on. If accepted, someone from the lodge (often the
                                            Secretary) contacts the candidate and informs him that he has been accepted
                                            and schedules a date for the Entered Apprentice degree.<br>
                                            <br>
                                            NOTE: This is based on the summation of several experiences in the U.S. Your
                                            experience may vary.<br>
                                        </p>
