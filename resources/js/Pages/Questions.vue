                                        <h2 class="text-xl font-semibold text-primary-600">Who is the head of all the
                                            Masons?</h2>
                                        <p class="mt-2 ">
                                            No one. Each Grand Lodge has its own jurisdiction and is the supreme
                                            authority within that jurisdiction.<br>
                                            <br>
                                            Obviously, many Grand Lodges have regular communication with each other, but
                                            official policy in one has no effect in another.<br>
