                                        <h2 class="text-xl font-semibold text-primary-600">Is Masonry a secret
                                            society?</h2>
                                        <p class="mt-2">
                                            No. Secret societies are generally defined as organizations which are
                                            unknown to the public and whose existence is denied. The Bavarian Illuminati
                                            and the Mafia would be examples of secret societies.<br>
                                            <br>
                                            Masonry, on the other hand, is well-known and proudly displays its
                                            existence. Masonic Temples are clearly marked as such, and many Lodges are
                                            listed in the yellow pages (usually under "Fraternal Orders"). Members often
                                            wear rings or tie-clips that identify themselves as Masons, and Masons often
                                            participate in community charity work. Finally, some Masonic functions are
                                            open to the public.<br>
                                            <br>
                                            Masonry is not a secret society, but rather a society with a few secrets.
                                            These are mainly modes of recognition - the signals, grips, signs, and
                                            phrases by which Masons recognize each other. The actual degree rituals are
                                            considered secret as well, not because there is anything that would harm
                                            Masonry by their revelation, but rather because they are more meaningful if
                                            the candidate does not know what is going to go on during them beforehand.<br>
                                            <br>
                                            It should be pointed out that many other organizations have a similar class
                                            of secrets. College fraternities (a.k.a. "Greek letter organizations") often
                                            have small secrets known only to their members, allowing them to travel from
                                            house to house and still be known.<br>
                                        </p>
