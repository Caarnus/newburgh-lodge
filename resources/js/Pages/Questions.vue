                                        <h2 class="text-xl font-semibold text-primary-600">Are Masonic rituals demeaning
                                            or embarrassing to the candidate?</h2>
                                        <p class="mt-2">
                                            Nothing could be further from the truth. The rituals (degrees) are designed
                                            to reinforce virtues that the Craft finds desirable, such as Justice,
                                            Brotherly Love, Truth, and the like. The rituals are actually quite
                                            beautiful and filled with ancient language and much symbolism. At no point,
                                            however, is the candidate asked to do anything that would embarrass or
                                            demean him, nor anything that would violate his obligations to his faith,
                                            country, or the law.<br>
