                                        <h2 class="text-xl font-semibold text-primary-600">From where did Masons
                                            come?</h2>
                                        <p class="mt-2">
                                            A fascinating question! And, alas, impossible to answer within the confines
                                            of this FAQ. There are a number of theories, a lot of debate, and a lot of
                                            musty history books. Some of the books listed in question 15 of this section
                                            should be of help. As a very brief overview, here is part of an essay by
                                            Henry C. Clausen, a noted Masonic author. This is, of course, just one point
                                            of view - many other theories exist, but Clausen nicely covers the basics:<br>
                                            <br>
                                            "Our Masonic antiquity is demonstrated by a so-called Regius Manuscript
                                            written around the year 1390, when King Richard II reigned in England, a
                                            century before Columbus. It was part of the King's Library that George II
                                            presented to the British Museum in 1757. Rediscovered by James O. Halliwell,
                                            a non-Mason, and rebound in its present form in 1838, it consists of 794
                                            lines of rhymed English verse and claims there was an introduction of
                                            Masonry into England during the reign of Athelstan, who ascended the throne
                                            in A.D. 925. It sets forth regulations for the Society, fifteen articles and
                                            fifteen points and rules of behavior at church, teaching duties to God and
                                            Church and Country, and inculcating brotherhood. While the real roots of
                                            Masonry are lost in faraway mists, these items show that our recorded
                                            history goes back well over 600 years."<br>
                                        </p>
