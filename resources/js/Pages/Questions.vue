                                            path by which one reaches the after-life; a theology which attempts to
                                            describe the nature of God; and the description of ways or practices by
                                            which a man or woman may seek to communicate with God. Masonry does none of
                                            those things. We offer no plan of salvation. With the exception of saying
                                            that He is a loving Father who desires only good for His children, we make
                                            no effort to describe the nature of God. And while we open and close our
                                            meetings with prayer, and we teach that no man should ever begin any
                                            important undertaking without first seeking the guidance of God, we never
                                            tell a man how he should pray or for what he should pray. Instead, we tell
                                            him that he must find the answers to these great questions in his own faith,
